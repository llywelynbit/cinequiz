// Normalize string for flexible matching (remove punctuation, lowercase, etc.)
function normalizeString(str: string): string {
  return str
    .toLowerCase()
    .replace(/[^\w\s]/g, '') // Remove punctuation
    .replace(/\s+/g, ' ') // Normalize whitespace
    .trim();
}

export async function searchMovies(query: string): Promise<MovieSuggestion[]> {
  if (!query || query.trim().length < 2) {
    return [];
  }

  // Lazy-load database on first search
  const database = getMovieDatabase();
  
  // For development: Use mock data with flexible matching
  const normalizedQuery = normalizeString(query);
  const results = database
    .filter(movie => {
      const normalizedTitle = normalizeString(movie.title);
      return normalizedTitle.includes(normalizedQuery);
    })
    .slice(0, 8); // Limit to 8 suggestions

  return new Promise(resolve => {
    setTimeout(() => resolve(results), 100); // Simulate API delay
  });
}
