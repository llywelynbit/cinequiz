// Get the challenge for today based on days since epoch
export function getTodayChallenge(): DailyChallenge {
  const epochDate = new Date('2024-01-01').getTime();
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const daysSinceEpoch = Math.floor((today.getTime() - epochDate) / (1000 * 60 * 60 * 24));
  const challengeIndex = daysSinceEpoch % dailyChallenges.length;
  
  return {
    ...dailyChallenges[challengeIndex],
    challengeNumber: daysSinceEpoch + 1,
  };
}